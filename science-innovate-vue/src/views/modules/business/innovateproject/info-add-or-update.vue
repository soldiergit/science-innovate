<template>
  <el-dialog
    :title="!dataForm.ipId ? '新增' : '修改'"
    :close-on-click-modal="false"
    :visible.sync="visible">
    <el-row :gutter="20">
      <el-form :model="dataForm" :rules="dataRule" ref="dataForm" @keyup.enter.native="dataFormSubmit()"
               label-width="16rem"
               style="width: 94%; margin: 0 auto"
               v-loading="dataFormLoading"
               element-loading-text="拼命加载中"
               element-loading-spinner="el-icon-loading">
        <el-col :span="24">
          <el-form-item label="项目名称" prop="name">
            <el-input v-model="dataForm.name" placeholder="项目名称"></el-input>
          </el-form-item>
        </el-col>
        <el-col :span="24">
          <el-form-item label="项目组成员" prop="entryStu">
            <el-input v-model="dataForm.entryStu" placeholder="项目组成员"></el-input>
          </el-form-item>
        </el-col>
        <el-col :span="24">
          <el-form-item label="经费/万元" prop="money">
            <el-input-number v-model="dataForm.money" :precision="1" :step="0.1" :min="1"></el-input-number>
          </el-form-item>
        </el-col>
        <el-col :span="24">
          <el-form-item label="第一指导老师" prop="firstTeacherId">
            <el-select v-model="dataForm.firstTeacherId" style="width:50%" filterable placeholder="请选择">
              <el-option v-for="item in teacherList"
                         :key="item.teacherId"
                         :label="item.name"
                         :value="item.teacherId"
                         :disabled="item.disabled">
              </el-option>
            </el-select>
          </el-form-item>
        </el-col>
        <el-col :span="24">
          <el-form-item label="其它指导老师" prop="otherTeachersId">
            <el-select v-model="dataForm.otherTeachersId" style="width:50%" multiple filterable placeholder="请选择">
              <el-option v-for="item in teacherList"
                         :key="item.teacherId"
                         :label="item.name"
                         :value="item.teacherId">
              </el-option>
            </el-select>
          </el-form-item>
        </el-col>
        <el-col :span="24">
          <el-form-item label="立项时间" prop="awardTime">
            <!--时间：后台是date类型的写法-->
            <el-date-picker
              value-format="yyyy-MM-dd HH:mm:ss"
              type="year"
              v-model="dataForm.awardTime"
              placeholder="选择立项时间">
            </el-date-picker>
          </el-form-item>
        </el-col>
        <el-col :span="24">
          <el-form-item label="立项级别" prop="grade">
            <el-select v-model="dataForm.grade" placeholder="请选择">
              <el-option v-for="item in competitionGradeList" :key="item.value" :label="item.label"
                         :value="item.value"></el-option>
            </el-select>
          </el-form-item>
        </el-col>
        <el-col :span="24">
          <el-form-item label="立项材料" prop="fileList">
            <el-upload
              class="upload-demo"
              :data="upLoadData"
              :action="upLoadUrl"
              :before-remove="beforeRemove"
              :on-remove="upLoadRemove"
              :on-success="upLoadSuccess"
              :file-list="fileList">
              <el-button size="small" type="primary">上传材料</el-button>
            </el-upload>
          </el-form-item>
        </el-col>
      </el-form>
    </el-row>
    <span slot="footer" class="dialog-footer">
      <el-button @click="visible = false">取消</el-button>
      <el-button type="primary" @click="dataFormSubmit()">确定</el-button>
    </span>
  </el-dialog>
</template>

<script>
  import {clearLoginInfo, transferFileToData, transferDataToFile, Attach} from '@/utils'

  export default {
    data () {
      var validateAttach = (rule, value, callback) => {
        if (this.fileList.length === 0) {
          callback(new Error('立项材料不能为空'))
        } else {
          let notNull = false
          for (let index = 0; index < this.fileList.length; index++) {
            if (this.fileList[index].isDel === 0) {
              notNull = true
            }
          }
          if (notNull) {
            callback()
          } else {
            callback(new Error('立项材料不能为空'))
          }
        }
      }
      return {
        visible: false,
        addLoading: false,
        dataFormLoading: false,
        competitionGradeList: this.$store.state.user.competitionGradeEntities,
        matchList: [],
        teacherList: [],
        // 文件上传路径
        upLoadUrl: this.$http.adornUrl(`/business/sysattach/upload`),
        // 文件上传携带的数据
        upLoadData: {
          'userId': this.$store.state.user.id,
          'fileType': '/IP/',
          'token': this.$cookie.get('token')
        },
        // 附件对象集合
        fileList: [],
        dataForm: {
          ipId: 0,
          name: '',
          entryStu: '',
          money: '',
          awardTime: '',
          grade: '',
          firstTeacherId: '',
          otherTeachersId: []
        },
        dataRule: {
          name: [
            {required: true, message: '项目名称不能为空', trigger: 'blur'}
          ],
          entryStu: [
            {required: true, message: '项目组成员不能为空', trigger: 'blur'}
          ],
          money: [
            {required: true, message: '项目经费不能为空', trigger: 'blur'}
          ],
          awardTime: [
            {required: true, message: '立项时间不能为空', trigger: 'blur'}
          ],
          grade: [
            {required: true, message: '立项级别', trigger: 'blur'}
          ],
          firstTeacherId: [
            {required: true, message: '第一指导老师不能为空', trigger: 'blur'}
          ],
          fileList: [
            {required: true, validator: validateAttach, trigger: 'blur'}
          ]
        }
      }
    },
    methods: {
      init (id) {
        this.dataForm.ipId = id || 0
        this.visible = true
        this.dataFormLoading = true
        this.$nextTick(() => {
          // 重置表单 如果想让resetFields方法有效果，必须要在el-form-item上增加prop字段，且和v-model的属性对应
          this.$refs['dataForm'].resetFields()
          this.matchList = []
          this.teacherList = []
          // 可选择的指导老师永远为全体教师
          this.teacherList = this.$store.state.user.allTeacherSelectList
          this.fileList = []
          // 修改
          if (this.dataForm.ipId) {
            this.$http({
              url: this.$http.adornUrl(`/business/innovateproject/info/${this.dataForm.ipId}`),
              method: 'get',
              params: this.$http.adornParams()
            }).then(({data}) => {
              if (data && data.code === 0) {
                this.dataForm.name = data.innovateProject.name
                this.dataForm.entryStu = data.innovateProject.entryStu
                this.dataForm.money = data.innovateProject.money
                this.dataForm.awardTime = data.innovateProject.awardTime
                this.dataForm.grade = data.innovateProject.grade
                this.dataForm.firstTeacherId = data.innovateProject.firstTeacherId
                this.dataForm.otherTeachersId = data.innovateProject.otherTeachersId
                // 附件信息
                this.fileList = transferDataToFile(data.innovateProject.sysAttachEntityList)
                this.dataFormLoading = false
              } else {
                this.dataFormLoading = false
              }
            })
          } else {
            this.dataFormLoading = false
          }
        })
      },
      // 表单提交
      dataFormSubmit () {
        this.$refs['dataForm'].validate((valid) => {
          if (valid) {
            this.$http({
              url: this.$http.adornUrl(`/business/innovateproject/${!this.dataForm.ipId ? 'save' : 'update'}`),
              method: 'post',
              data: this.$http.adornData({
                'ipId': this.dataForm.ipId || undefined,
                'name': this.dataForm.name,
                'entryStu': this.dataForm.entryStu,
                'money': this.dataForm.money,
                'awardTime': this.dataForm.awardTime,
                'grade': this.dataForm.grade,
                'firstTeacherId': this.dataForm.firstTeacherId,
                'otherTeachersId': this.dataForm.otherTeachersId,
                'sysAttachEntityList': transferFileToData(this.fileList)
              })
            }).then(({data}) => {
              if (data && data.code === 0) {
                this.$message({
                  message: '操作成功',
                  type: 'success',
                  duration: 1500,
                  onClose: () => {
                    this.addLoading = false
                    this.visible = false
                    this.$emit('refreshDataList')
                  }
                })
              } else {
                this.$message.error(data.msg)
                this.addLoading = true
              }
            })
          }
        })
      },
      // 移除附件
      beforeRemove (file, fileList) {
        return this.$confirm(`确定移除 ${file.name}？`)
      },
      // 移除附件
      upLoadRemove (file, fileList) {
        this.$http({
          url: this.$http.adornUrl('/business/sysattach/deleteFile'),
          method: 'post',
          params: this.$http.adornParams({
            'filePath': file.url
          })
        }).then(({data}) => {
          if (data && data.code === 0) {
            this.fileList = fileList
          } else {
            this.$message.error(data.msg)
          }
        })
      },
      // 文件上传成功后回调
      upLoadSuccess (data) {
        if (data && data.code === 0) {
          // 将回调的后台文件对象转为前端的文件对象
          this.fileList.push(new Attach(data.sysAttachEntity))
        } else {
          this.$alert(data.msg, '提示', {
            confirmButtonText: '确定',
            type: 'error'
          }).then(() => {
            this.$http({
              url: this.$http.adornUrl('/sys/logout'),
              method: 'post',
              data: this.$http.adornData()
            }).then(({data}) => {
              if (data && data.code === 0) {
                clearLoginInfo()
                this.$router.push({name: 'login'})
              }
            })
          })
        }
      }
    }
  }
</script>
